<h3 id='LDOC'>  Duke IEEE LDOC Sign </h3>
To raise awareness and promote Duke's IEEE student branch, our executive board made an LED sign that was presented
at Duke's Last Day of Classes (LDOC) celebration. The number of lit LEDs in the sign is proportional to the sound
picked up by the microphone, so the sign acts as an audio equalizer. The sign is composed of strips of LEDs that 
are connected to several microcontrollers. I worked several hours every day for almost a month to help design, build,
and debug the sign. A picture of the completed sign is below, and the video shows the sign working during the 
performance of hip-hop artist Mackelmore. 

